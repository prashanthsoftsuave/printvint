<h4>Application Logs</h4>
<p>Some personal information may be stored in application logs and is kept for debugging and troubleshooting purposes.</p>

<h3>How long we retain your data</h3>
<h4>Plugin Settings</h4>
<p>Plugin settings are kept indefinitely until removed by website owner.</p>

<h4>Application Logs</h4>
